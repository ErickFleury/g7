public class Veiculo {
    private String placa;
    private String marca;
    private String modelo;
    private String cor;
    private String tipo;

    public Veiculo(String placa, String marca, String modelo, String cor, String tipo) {
        this.placa = placa;
        this.marca = marca;
        this.modelo = modelo;
        this.cor = cor;
        this.tipo = tipo;
    }
}

